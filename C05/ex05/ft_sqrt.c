int	ft_sqrt(int nb)
{
	unsigned int idx;

	if (nb <= 0)
		return (0);
	if (nb <= 1)
		return (nb);
	idx = 1;
	while (idx * idx <= nb)
	{
		if (idx * idx == nb)
			return ((int)idx);
		idx++;
	}
	return (0);
}
